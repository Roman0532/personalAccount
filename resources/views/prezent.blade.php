            <h3>
                <center>Краткие сведения о кафедре Прикладной математики</center>
            </h3>
            <div class="posts_content"><br>
                <p align="justify">В целях оптимизации структуры математического
                    факультета, в мае 2015 года в КемГУ вышли подряд два приказа (№245/10
                    от 18.05.2015 г. о переименовании с 19.05.2015 г. кафедры
                    математической кибернетики (МК) в кафедру прикладной математики (ПМ) и
                    ликвидации кафедр высшей математики, вычислительной математики и
                    автоматизации исследований и технической кибернетики (АИТК); №1431/08
                    от 21.05.2015 г. о переводе преподавателей кафедры АИТК на кафедру ПМ),
                    определившие образование новой кафедры ПМ, в состав которой вошли все
                    работники МК и АИТК.</p>
                <p align="justify"> Профессорско-преподавательский состав кафедры
                    прикладной математики: профессоров – 3, доцентов – 6, старших
                    преподавателей – 4.</p>

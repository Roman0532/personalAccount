            <div class="posts_content"><br>
                <p align="justify"><span>Прикладная математика</span> - это применение математических методов и
                    алгоритмов в самых различных сферах жизнедеятельности человека. </p>
                <p align="justify">
                    Применение математики к любой практической проблеме начинается с
                    построения математической модели. Имея задачу, сформулированную
                    математически, можно выбрать алгоритм ее решения из множества уже
                    имеющихся, либо разработать новый алгоритм. Умение строить и
                    анализировать математические модели, выбор и разработка методов решения
                    - все это навыки математика-прикладника.</p>
                <p align="justify">
                    Сегодня ни одна серьезная практическая задача не решается без
                    использования вычислительной техники. Для прикладника, компьютер - это
                    инструмент, с помощью которого он реализует решение задачи, примерно,
                    как ручка для письма или ложка для еды. Под «компьютером» понимается
                    как знание языков и средств, так и специализированных пакетов программ
                    для различных предметных областей.</p>

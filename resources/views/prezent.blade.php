            </h3>
            <div class="posts_content"><br>
                <p align="justify"> Известно, что сегодня на рынке труда высоко ценятся
                    программисты. Однако, еще больше ценятся (в том числе и за рубежом)
                    программисты, получившие фундаментальную математическую базу. Такую
                    базу (а не просто умение писать код) можно получить на профильных
                    кафедрах классических ВУЗов, каковой является кафедра Прикладной
                    математики Кемеровского Государственного университета. Однако,
                    возможности трудоустройства выпускников-прикладников не ограничиваются

                    Таким образом, дата 19 мая 2015 года стала, фактически, вторым днем
                    рождения кафедры ПМ. Первый раз кафедра ПМ была открыта на МФ КемГУ в
                    1979 году и просуществовала до 1992 года, когда по приказу №477/08
                    ректора КемГУ от 10.06.1992 г. она была разделена на две кафедры – МК и
                    АИТК. По этому же приказу были назначены заведующими кафедрами
                    соответственно профессор Данилов Н.Н. и доцент Карташов В.Я.

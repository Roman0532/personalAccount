                <p align="justify">За тринадцать лет (со времени распада кафедры ПМ до
                    ее возрождения) эти две кафедры оставались родственными, участвуя в
                    реализации одной и той же специальности (направления) Прикладная
                    математика и информатика, читая основные курсы одному и тому же
                    контингенту студентов, проводя совместно различные виды практик
                    студентов. За эти годы обе кафедры воспитали четыре доктора наук, более
                    двадцати кандидатов наук, выпустили целую плеяду
                    высококвалифицированных специалистов по прикладной математике и
                    информатике, которые успешно трудятся в Кемеровской области и в других
                    регионах России, прославляя свои кафедры, факультет и университет.</p>
                <p align="justify">На вновь образованной кафедре прикладной математики
